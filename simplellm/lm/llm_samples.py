def parse_sample_text(sample_text: str) -> list[str]:
    samples = sample_text.split("\n")
    samples = [x.strip() for x in samples]
    samples = [(x.split(' ')[1] if ' ' in x else x) for x in samples]
    samples = [s for s in samples if len(s) > 0]
    return samples

